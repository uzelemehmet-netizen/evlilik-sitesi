export function phrasebookTranslate(text, targetLang) {
  const key = norm(text);
  if (!key) return '';

  if (targetLang === 'id') {
    return TR_TO_ID.get(key) || '';
  }
  if (targetLang === 'tr') {
    return ID_TO_TR.get(key) || '';
  }

  return '';
}

export function phrasebookCount() {
  return TR_TO_ID.size;
}
