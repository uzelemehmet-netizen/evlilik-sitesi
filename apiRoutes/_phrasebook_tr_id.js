function norm(s) {
  return String(s || '')
    .trim()
    .toLowerCase()
    .replace(/[\u200B-\u200D\uFEFF]/g, '')
    .replace(/\s+/g, ' ')
    .replace(/[؟?!.…]+$/g, '')
    .replace(/\s*[،,:;]+\s*$/g, '');
}

